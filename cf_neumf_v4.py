class BPRDataset(Dataset):
    """
    Original pairwise BPR dataset with uniform negative sampling.
    Kept for ablation comparison.
    """
    def __init__(self, train_df, user_pos, n_items):
        self.pairs    = list(zip(train_df['user'].tolist(), train_df['item'].tolist()))
        self.user_pos = user_pos
        self.n_items  = n_items
    def __len__(self): return len(self.pairs)
    def __getitem__(self, idx):
        u, pos = self.pairs[idx]
        neg = random.randint(0, self.n_items - 1)
        while neg in self.user_pos[u]:
            neg = random.randint(0, self.n_items - 1)
        return (torch.tensor(u, dtype=torch.long),
                torch.tensor(pos, dtype=torch.long),
                torch.tensor(neg, dtype=torch.long))


def train_bpr(
    model, train_df, user_pos, n_items,
    n_epochs=50, batch_size=2048, lr=1e-3,
    weight_decay=1e-5, device=DEVICE
):
    """
    Original BPR trainer with uniform negatives.
    Loss: L = -mean(log σ(score_pos - score_neg))
    Kept for ablation comparison against improved variant.
    """
    dataset   = BPRDataset(train_df, user_pos, n_items)
    loader    = DataLoader(dataset, batch_size=batch_size, shuffle=True,
                           num_workers=0, pin_memory=(device.type == 'cuda'))
    optimizer = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=weight_decay)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=n_epochs, eta_min=1e-5)
    model.train()
    for epoch in range(1, n_epochs + 1):
        total_loss = 0.0
        for users, pos_items, neg_items in loader:
            users, pos_items, neg_items = users.to(device), pos_items.to(device), neg_items.to(device)
            pos_scores = model(users, pos_items)
            neg_scores = model(users, neg_items)
            loss = -torch.log(torch.sigmoid(pos_scores - neg_scores) + 1e-8).mean()
            optimizer.zero_grad(set_to_none=True)
            loss.backward()
            nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()
            total_loss += loss.item() * len(users)
        scheduler.step()
        if epoch % 5 == 0 or epoch == 1:
            print(f"  Epoch {epoch:3d}/{n_epochs} | BPR Loss: {total_loss/len(dataset):.5f} | LR: {scheduler.get_last_lr()[0]:.2e}")
    return model

class ItemKNN:
    """Item-based nearest-neighbour CF. Similarity = adjusted cosine."""
    def __init__(self, k=50):
        self.k = k; self.sim = None; self.user_history = {}
    def fit(self, train_df, n_users, n_items, user_pos):
        self.user_history = {u: list(items) for u, items in user_pos.items()}
        R = np.zeros((n_users, n_items), dtype=np.float32)
        for _, row in train_df.iterrows():
            R[int(row['user']), int(row['item'])] = 1.0
        norms = np.linalg.norm(R, axis=0, keepdims=True)
        norms[norms == 0] = 1.0
        R_norm = R / norms
        print(f"  Computing {n_items}x{n_items} item-item similarity matrix...")
        sim = R_norm.T @ R_norm
        np.fill_diagonal(sim, 0.0)
        if self.k < n_items - 1:
            for i in range(n_items):
                row = sim[i]
                threshold = np.partition(row, -self.k)[-self.k]
                sim[i][row < threshold] = 0.0
        self.sim = sim
        print(f"  ItemKNN fitted. k={self.k}")
        return self
    def score_candidates(self, user, candidates):
        hist = self.user_history.get(user, [])
        if not hist or self.sim is None:
            return np.zeros(len(candidates), dtype=float)
        return self.sim[np.array(candidates, dtype=int)][:, np.array(hist, dtype=int)].sum(axis=1)

"""## 4. ★ Improved NeuMF Training Infrastructure ★

Three synergistic improvements over the baseline:

| Improvement | Why it helps |
|---|---|
| **BPR pairwise loss** | Directly maximises P(score_pos > score_neg) — aligns loss with ranking objective. BCE optimises absolute scores, not ordering. |
| **Popularity-weighted negatives** (freq^0.75) | Popular items the user hasn't rated are harder to reject than obscure ones. Harder negatives = more informative gradients from the first epoch. |
| **DNS after warmup** | Once the model has learned non-trivial scores (after warmup), we score dns_k candidate negatives per positive and pick the hardest. Steeper gradient → faster convergence to better optima. Warmup prevents DNS with an untrained model (random scores = random "hard" negatives = noise). |
"""

def build_pop_weights(train_df: pd.DataFrame, n_items: int,
                       alpha: float = 0.75) -> np.ndarray:
    """
    Popularity-weighted sampling distribution ∝ item_freq^alpha.
    alpha=0.75 (word2vec standard) smooths dominance of top items
    while still making popular items significantly more likely negatives.
    Returns a probability array over all n_items.
    """
    counts = train_df['item'].value_counts()
    freq   = np.array([counts.get(i, 0) for i in range(n_items)], dtype=float)
    w      = np.power(freq + 1, alpha)   # +1 avoids zero weights
    return (w / w.sum())


def train_neumf_improved(
    model,
    train_df:       pd.DataFrame,
    user_pos:       dict,
    n_items:        int,
    n_epochs:       int   = 50,
    batch_size:     int   = 2048,
    lr:             float = 5e-4,
    weight_decay:   float = 1e-5,
    warmup_epochs:  int   = 15,
    dns_k:          int   = 8,
    device:         torch.device = DEVICE
) -> nn.Module:
    """
    Improved NeuMF training with three stacked improvements:

    Phase 1 — epochs [1, warmup_epochs]:
        Loss : BPR  →  L = -log σ(score_pos - score_neg)
        Negs : Popularity-weighted sampling (freq^0.75)

    Phase 2 — epochs [warmup+1, n_epochs]:
        Loss : BPR  (same)
        Negs : DNS (Dynamic Negative Sampling)
               For each positive in the batch:
                 1. Sample dns_k candidate negatives (pop-weighted)
                 2. Score all in one batched forward pass
                 3. Select the highest-scoring (hardest) as the negative
               DNS is batched → single forward pass per training batch,
               not per sample.  GPU-friendly and adds minimal overhead.

    WHY WARMUP BEFORE DNS:
        An untrained model assigns nearly random scores, so the "hardest"
        DNS candidate is random noise.  Warmup lets the model develop
        meaningful representations first, after which DNS provides truly
        harder and more informative negatives.
    """
    pop_weights = build_pop_weights(train_df, n_items)
    positives   = list(zip(train_df['user'].tolist(), train_df['item'].tolist()))

    optimizer = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=weight_decay)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
        optimizer, T_max=n_epochs, eta_min=1e-5
    )

    for epoch in range(1, n_epochs + 1):
        random.shuffle(positives)
        model.train()
        use_dns    = (epoch > warmup_epochs)
        total_loss = 0.0
        n_batches  = 0

        for start in range(0, len(positives), batch_size):
            batch = positives[start : start + batch_size]
            if not batch:
                continue
            users_list = [x[0] for x in batch]
            pos_list   = [x[1] for x in batch]
            B          = len(batch)

            # ── Negative sampling ─────────────────────────────────────────────
            if use_dns:
                # Batch-level DNS: score dns_k candidates per positive,
                # then select the hardest (highest model score) for each user.
                all_cands = []
                for u in users_list:
                    cands = []
                    while len(cands) < dns_k:
                        c = int(np.random.choice(n_items, p=pop_weights))
                        if c not in user_pos[u]:
                            cands.append(c)
                    all_cands.append(cands)

                # One batched forward pass to score all candidates
                model.eval()
                with torch.no_grad():
                    flat_u = [u for u, cs in zip(users_list, all_cands) for _ in cs]
                    flat_i = [c for cs in all_cands for c in cs]
                    u_t = torch.tensor(flat_u, dtype=torch.long, device=device)
                    i_t = torch.tensor(flat_i, dtype=torch.long, device=device)
                    dns_scores = model(u_t, i_t).cpu().numpy().reshape(B, dns_k)
                model.train()

                neg_list = [cs[int(np.argmax(sc))]
                            for cs, sc in zip(all_cands, dns_scores)]
            else:
                # Phase 1: popularity-weighted uniform sampling
                neg_list = []
                for u in users_list:
                    c = int(np.random.choice(n_items, p=pop_weights))
                    while c in user_pos[u]:
                        c = int(np.random.choice(n_items, p=pop_weights))
                    neg_list.append(c)

            # ── BPR loss ──────────────────────────────────────────────────────
            users_t = torch.tensor(users_list, dtype=torch.long, device=device)
            pos_t   = torch.tensor(pos_list,   dtype=torch.long, device=device)
            neg_t   = torch.tensor(neg_list,   dtype=torch.long, device=device)

            optimizer.zero_grad(set_to_none=True)
            pos_scores = model(users_t, pos_t)
            neg_scores = model(users_t, neg_t)
            loss = -torch.log(torch.sigmoid(pos_scores - neg_scores) + 1e-8).mean()
            loss.backward()
            nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()

            total_loss += loss.item()
            n_batches  += 1

        scheduler.step()
        if epoch % 5 == 0 or epoch == 1:
            avg = total_loss / max(n_batches, 1)
            tag = "DNS+pop" if use_dns else "pop-weighted"
            print(f"  Epoch {epoch:3d}/{n_epochs} | BPR Loss: {avg:.5f} | "
                  f"LR: {scheduler.get_last_lr()[0]:.2e} | Neg: {tag}")
    return model

