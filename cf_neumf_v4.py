@torch.no_grad()
def evaluate_with_llm_reranking(
    neuMF_model, test_df, eval_negs, user_history,
    idx2item, item_titles,
    Ks=(5, 10, 15, 20), n_rerank=20,
    api_key=None, device=DEVICE, max_users=None
):
    """
    NeuMF + LLM (Claude) re-ranking.
    Top-n_rerank candidates by NeuMF score are re-ordered by the LLM.
    Falls back to NeuMF-only if no API key is available.
    """
    key     = api_key or os.environ.get('ANTHROPIC_API_KEY')
    use_llm = False; client = None
    if key:
        try:
            import anthropic
            client  = anthropic.Anthropic(api_key=key); use_llm = True
            print(f"[LLM] Claude will rerank top-{n_rerank} per user.")
        except ImportError:
            print("[LLM] anthropic package not found. pip install anthropic")
    else:
        print("[LLM] No ANTHROPIC_API_KEY found. Falling back to NeuMF scores.")

    neuMF_model.eval()
    results = {f'NDCG@{k}': [] for k in Ks}
    results.update({f'HR@{k}': [] for k in Ks})
    n_ok = n_fail = 0

    for eval_idx, (_, row) in enumerate(test_df.iterrows()):
        if max_users is not None and eval_idx >= max_users: break
        u, pos_item = int(row['user']), int(row['item'])
        candidates  = [pos_item] + eval_negs[u]
        u_t    = torch.tensor([u]*100, dtype=torch.long, device=device)
        i_t    = torch.tensor(candidates, dtype=torch.long, device=device)
        scores = torch.sigmoid(neuMF_model(u_t, i_t)).cpu().numpy()
        nmf_order = np.argsort(scores)[::-1].tolist()

        if use_llm:
            top_cand_idx  = nmf_order[:n_rerank]
            rest_cand_idx = nmf_order[n_rerank:]
            top_item_ids  = [candidates[ci] for ci in top_cand_idx]
            hist_titles   = [item_titles.get(idx2item.get(it,-1),'Unknown')
                             for it in user_history[u][-5:]]
            cand_info     = [f"item_id={iid}: {item_titles.get(idx2item.get(iid,-1),'Unknown')}"
                             for iid in top_item_ids]
            prompt = (
                f"A user recently enjoyed: {', '.join(hist_titles)}.\n\n"
                f"Rank the following {n_rerank} movies from MOST to LEAST relevant.\n"
                f"Output ONLY a JSON array of item_ids. Example: [42,7,15]\n\n"
                "Candidates:\n" + "\n".join(cand_info)
            )
            llm_reranked = top_cand_idx
            try:
                resp     = client.messages.create(
                    model='claude-haiku-4-5-20251001', max_tokens=256,
                    messages=[{'role':'user','content':prompt}])
                raw = resp.content[0].text.strip()
                s, e = raw.find('['), raw.rfind(']')+1
                if s != -1 and e > s:
                    ranked_ids = json.loads(raw[s:e])
                    id2pos = {candidates[ci]: ci for ci in top_cand_idx}
                    llm_reranked = [id2pos[r] for r in ranked_ids if r in id2pos]
                    seen = set(llm_reranked)
                    for ci in top_cand_idx:
                        if ci not in seen: llm_reranked.append(ci)
                n_ok += 1
            except Exception: n_fail += 1
            final_order = llm_reranked + rest_cand_idx
        else:
            final_order = nmf_order

        pos_rank = final_order.index(0) + 1
        for k in Ks:
            results[f'HR@{k}'].append(1 if pos_rank <= k else 0)
            results[f'NDCG@{k}'].append(1.0/np.log2(pos_rank+1) if pos_rank<=k else 0.0)

    if use_llm:
        print(f"[LLM] {n_ok}/{n_ok+n_fail} calls succeeded.")
    return {key: float(np.mean(vals)) for key, vals in results.items()}


